
# Subscripting - Pulls out the letter at the index 4, which is "o"
print("Hello"[4])

# String
print("123" + "345")

# Integer - A Whole Number
print(123 + 345)

# Large Integers
# Can use underscores (_) between numbers to replace commas for thousands, millions, etc
print(123_456_789)

# Float - Numbers that have a decimal value with it
print(3.14159)

# Boolean - True OR False
print(True)
print(False)
