
print("My age: " + str(12))
print(5 + 3)   # Addition
print(5 - 3)   # Subtraction
print(5 * 3)   # Multiplication
print(5 / 3)   # Division
print(5 // 3)  # Division, but forced to be an Integer
print(5 ** 3)  # Exponents (5 to the Power of 3)
print("")

# PEMDAS - The Order Of Priority in Mathematical Operations

# ()   : Parenthesis
# **   : Exponents
# * /  : Multiplication & Division (whichever comes first)
# + -  : Addition & Subtraction (whichever comes first)

print(3 * (3 + 3) / 3 - 3)
